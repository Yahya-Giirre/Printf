#include "ft_printf.h"

void	ft_str(char *str, int *len)
{
	int	i;

	i = 0;
	if (!str)
	{
		write(1, "null", 6);
		(*len) += 6;
		return (0);
	}
	while (str[i])
	{
		ft_putchar_len(str[i], len);
		i++;
	}
}
